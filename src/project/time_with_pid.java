    public void showTableData(){
        try
        {
          Connection  con=DriverManager.getConnection("jdbc:mysql://localhost:3306/pro?zeroDateTimeBehavior=convertToNull","root","MyS@L2");
            String sql="select * from prdinfo order by pid";
            Statement st=con.prepareStatement(sql);
            ResultSet rs=st.executeQuery(sql);
            DefaultTableModel tm;
            tm = ( DefaultTableModel)time_with_pid.getModel();
           time_with_pid.setModel(DbUtils.resultSetToTableModel(rs));
            tm.setRowCount(0);
            while(rs.next()){
            String o[] = {rs.getString("Period_id"),rs.getString("starting_time"),rs.getString("fid"),rs.getString("Ending_time")};
            time_with_pid.setModel(DbUtils.resultSetToTableModel(rs));
            tm.addRow(o);
            } 
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this,"can't open table");
        }
    } 
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
